public class 概述 {
    /**
     * Annotation
     * 是一种引用数据类型
     * 作用：规范代码
     *
     */

    /**
     * 定义注解
     * 修饰列表  @interface 注解名{
     *
     * }
     *
     * 使用时直接放在要使用的代码上面就可以了
     */

    /**
     * 常见注解
     * @Override：被注解的方法打算重写方法
     * @Deprecated：被注解的代码已经过时或者有更好的方法
     * @SuppressWarnings    抑制编译器警告
     * @SafeVarargs         “堆污染警告”
     * @FunctionalInterface     函数式接口
     *
     *
     * 元注解：注解注解的注解
     * @Target：表明被标注的注解可以出现的位置
     * @Retention：表明被标注的注解最终保存的位置
     */
}
