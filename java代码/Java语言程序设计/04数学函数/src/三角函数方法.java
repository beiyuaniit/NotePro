public class 三角函数方法 {
    public static void main(String[] args) {
        //正弦
        double a=Math.sin(0.5235987756);

        //余弦
        double b=Math.cos(Math.PI);

        //正切
        double c=Math.tan(1.57079632);

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
    }
}
