public class 指数函数方法 {
    public static void main(String[] args) {
        //返回值double

        //e的x次方
        double a=Math.exp(2);

        //以10为底的对数
        double b=Math.log10(100);

        //指数
        double c=Math.pow(2,3);

        //开方
        double d=Math.sqrt(4);

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);


    }
}
